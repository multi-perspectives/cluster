package org.emftext.term.propositional.expression.resource.expression.ui;

public class ExpressionQuickAssistAssistant extends org.eclipse.jface.text.quickassist.QuickAssistAssistant implements org.eclipse.jface.text.quickassist.IQuickAssistAssistant {
	
	public ExpressionQuickAssistAssistant(org.emftext.term.propositional.expression.resource.expression.IExpressionResourceProvider resourceProvider, org.emftext.term.propositional.expression.resource.expression.ui.IExpressionAnnotationModelProvider annotationModelProvider) {
		setQuickAssistProcessor(new org.emftext.term.propositional.expression.resource.expression.ui.ExpressionQuickAssistProcessor(resourceProvider, annotationModelProvider));
		setInformationControlCreator(new org.eclipse.jface.text.AbstractReusableInformationControlCreator() {
			public org.eclipse.jface.text.IInformationControl doCreateInformationControl(org.eclipse.swt.widgets.Shell parent) {
				return new org.eclipse.jface.text.DefaultInformationControl(parent, (org.eclipse.jface.text.DefaultInformationControl.IInformationPresenter) null);
			}
		});
	}
	
	public boolean canAssist(org.eclipse.jface.text.quickassist.IQuickAssistInvocationContext invocationContext) {
		return false;
	}
	
	public boolean canFix(org.eclipse.jface.text.source.Annotation annotation) {
		return true;
	}
	
}
